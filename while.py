print("IF STATEMENT")

spam = 0
if spam < 5:
    print('Spam is less than 5')
    spam = spam + 1


print('WHILE LOOP')

spam = 0
while spam < 5:
    print('Spam is less than 5')
    spam = spam + 1

# Both statements check the value of spam. If less then 5, they print a message
# BUT the while loop will repeat the message 5 times.
